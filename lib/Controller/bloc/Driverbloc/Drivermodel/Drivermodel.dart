class Driver {
  final String name;
  final String phone;
  final String driverId;
  final String email;
  final String image;
  final String timestamp;
  final bool isActive;

  Driver({
    required this.timestamp,
    required this.name,
    required this.phone,
    required this.driverId,
    required this.email,
    required this.image,
    required this.isActive,
  });
}
